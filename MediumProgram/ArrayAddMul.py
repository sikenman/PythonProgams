import numpy as np

# this program performs array addtion and multiplication using [numpy]

arr1 = np.array([1, 2, 3, 4, 5])
arr2 = np.array([5, 4, 3, 2, 1])

# Element-wise addition
result1 = arr1 + arr2

# Element-wise multiplication
result2 = arr1 * arr2

# Printing results
print(result1)
print(result2)
