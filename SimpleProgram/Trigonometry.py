import math

angleA = math.pi / 6
angleB = math.pi / 3

sin_value = math.sin(angleA)
cos_value = math.cos(angleB)
tan_value = math.tan(angleA)

print()
print(f"PI = {math.pi:1.10f}")
print(f"=================")
print(f"   Sine of {angleA:1.5f} =", f'{sin_value:1.5f}')
print(f" Cosine of {angleB:1.5f} =", f'{cos_value:1.5f}')
print(f"Tangent of {angleA:1.5f} =", f'{tan_value:1.5f}')
print()
